void solve(int tc = 1) {
  int n, k;
  cin >> n >> k;
  int diff = n - k;
  vector<int> arr;
  for (int i = 0; i < k; ++i) arr.push_back(i + 1);
  reverse(arr.begin() + (k - diff - 1), arr.end());
  for (int x : arr) cout << x << " ";
  cout << endl;
}

signed main() {
  cin.tie(nullptr);
  ios::sync_with_stdio(false);

  int tc = 1;
  cin >> tc;
  for (int t = 1; t <= tc; ++t) solve(t);

  return 0;
}